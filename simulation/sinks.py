class Sink:

    # ··················································································································

    def __init__(self, env):
        self.env = env
        self.name = "Sink"
        self.entities = list()
        self.log = list()

    # ··················································································································

    def add_log_entry(self, time,queue,amount):
        log_entry = {"time": time, "queue": queue, "amount": amount}
        self.log.append(log_entry)

